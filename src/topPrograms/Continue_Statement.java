package classDemo;

public class Continue_Statement {

	public static void main(String[] args) {
		for (int i=1; i<8; i++) {
			if (i==4) {
			continue;}
			System.out.println(i);
			}
		System.out.println("==================");
for (int j=1; j<8; j++) {
	if (j==4) {
	break;}
	System.out.println(j);
	}
}}

	
