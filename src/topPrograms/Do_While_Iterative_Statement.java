package classDemo;

public class Do_While_Iterative_Statement {

	public static void main(String[] args) {
		int count = 10;
		int i=0;
		do{
			System.out.println("count is "+count);
			count--;
		}while(count>=0);
	System.out.println("********************");
	do{
		System.out.println("count is "+i);
		i++;
	}while(i<=10);
}
}
