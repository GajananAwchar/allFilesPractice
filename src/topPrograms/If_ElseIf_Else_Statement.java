package classDemo;

public class If_ElseIf_Else_Statement {

	public static void main(String[] args) {
		int number=1000;
		if(number==0) {
			System.out.println("It is zero");
		}
		else if(number>0){
			System.out.println("It is a Positive Number");
		}
		else {
			System.out.println("It is a negative Number");
		}
	}

}
