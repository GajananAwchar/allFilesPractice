package javaBascis;

public class Data_DefaultValues {
	/*static variables
	print default values for Primitive data type
	for getting default values no need to assign values to the variables*/
	
static byte b1;
static short t1;
static int i1;
static long l1;
static float f1;
static double d1;
static boolean g1;
static char c;
	public static void main(String[] args) {
		System.out.println("The primitive Data type Defalut values are as :");
		System.out.println("for byte data types value is :" +b1);
		System.out.println("for short data types value is :" +t1);
		System.out.println("for int data types value is :" +i1);
		System.out.println("for long data types value is :" +l1);
		System.out.println("for float data types value is :" +f1);
		System.out.println("for double data types value is :" +d1);
		System.out.println("for boolean data types value is :" +g1);
		System.out.println("for char data types value is :" +c);
	}

}
